create extension if not exists pgcrypto;

create table if not exists public.archived_business_monthly (
  id bigint generated by default as identity primary key,
  customer_id bigint,
  customer_name text,
  mobile text,
  shop text not null default '',
  month_start date not null,
  rental_business numeric not null default 0,
  payments_received numeric not null default 0,
  round_off numeric not null default 0,
  opening_due numeric not null default 0,
  opening_credit numeric not null default 0,
  rental_count integer not null default 0,
  payment_count integer not null default 0,
  first_activity date,
  last_activity date,
  archived_at timestamp with time zone not null default now(),
  last_archive_run_id uuid,
  unique (customer_id, shop, month_start)
);

create table if not exists public.archived_tool_monthly (
  id bigint generated by default as identity primary key,
  tool_id bigint,
  tool_name text,
  shop text not null default '',
  month_start date not null,
  movement_count integer not null default 0,
  service_count integer not null default 0,
  service_cost numeric not null default 0,
  first_activity date,
  last_activity date,
  archived_at timestamp with time zone not null default now(),
  last_archive_run_id uuid,
  unique (tool_id, shop, month_start)
);

create table if not exists public.archived_shop_monthly (
  id bigint generated by default as identity primary key,
  shop text not null default '',
  month_start date not null,
  expense_total numeric not null default 0,
  expense_count integer not null default 0,
  sales_revenue numeric not null default 0,
  sales_cost numeric not null default 0,
  sales_profit numeric not null default 0,
  sale_count integer not null default 0,
  shop_cash_received numeric not null default 0,
  cash_entry_count integer not null default 0,
  first_activity date,
  last_activity date,
  archived_at timestamp with time zone not null default now(),
  last_archive_run_id uuid,
  unique (shop, month_start)
);

create table if not exists public.tt_archive_runs (
  id uuid primary key default gen_random_uuid(),
  cutoff_date date not null,
  status text not null default 'running',
  eligible_customer_count integer not null default 0,
  details jsonb not null default '{}'::jsonb,
  started_at timestamp with time zone not null default now(),
  completed_at timestamp with time zone
);

create table if not exists public.tt_backup_audit (
  id uuid primary key default gen_random_uuid(),
  backup_type text not null,
  status text not null,
  file_name text,
  drive_file_id text,
  row_count integer not null default 0,
  details jsonb not null default '{}'::jsonb,
  created_at timestamp with time zone not null default now()
);

create table if not exists public.tt_secure_settings (
  setting_key text primary key,
  encrypted_value text not null,
  updated_at timestamp with time zone not null default now()
);

create index if not exists archived_business_month_idx
  on public.archived_business_monthly (month_start, shop);

create index if not exists archived_business_customer_idx
  on public.archived_business_monthly (customer_id);

create index if not exists archived_tool_month_idx
  on public.archived_tool_monthly (month_start, tool_id);

create index if not exists archived_shop_month_idx
  on public.archived_shop_monthly (month_start, shop);

create index if not exists tt_backup_audit_created_idx
  on public.tt_backup_audit (created_at desc);

create index if not exists rentals_archive_lookup_idx
  on public.rentals (customer_id, end_date)
  where end_date is not null;

create index if not exists payments_archive_lookup_idx
  on public.payments (
    customer_id,
